void OLED_ShiftOut8Bit(unsigned char sendbyte)
{
		unsigned char j=8;
		OLED_SDA_OUT();
		for(;j>0;j--)
		{
				OLED_SCL_OFF;
				if(sendbyte >=0X80)
				{OLED_SDA_ON; }
				else
				OLED_SDA_OFF;
				sendbyte<<=1;	  
				delay_us(2);
				OLED_SCL_ON;
				delay_us(2);
		}
	 OLED_SCL_OFF;
		delay_us(2);		
		OLED_SCL_ON;
		delay_us(2);
		OLED_SCL_OFF;		
}
